## create gene sets from KEGG PATHWAYS

hsa.paths <- keggList("pathway", "hsa")
hsa.path.ids <- names(hsa.paths)
hsa.paths <- str_split(hsa.paths, " - Homo sapiens", simplify=TRUE)[,1]

xx <- hsa.path.ids
names(xx) <- hsa.paths

get.genes <- function(x) {
  tryCatch({
    dat <- keggGet(x)
    tmp <- dat[[1]]$GENE
    tmp <- tmp[grepl(";", tmp)]
    str_split(tmp, ";", simplify=TRUE)[,1]
  }, error=function(e) {
    cat("Error!", x)
    dat
  })
}

kegg <- lapply(xx, get.genes)
tmp <- kegg[sapply(kegg, is.list)] # after checking, basically these contain no GENE info
kegg <- kegg[!sapply(kegg, is.list)]
saveRDS(kegg, file="kegg.pathway.gsets.RDS")


